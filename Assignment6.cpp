#include<iostream>
using namespace std;

int main(){
    int a=10,b=5,sum,sub,mul,div,rem;

    sum=a+b;
    sub=a-b;
    mul=a*b;
    div=a/b;
    rem=a%b;

    cout << "Sum of Numbers :" << sum;
    cout << "\nSub of Numbers :" << sub;
    cout << "\nMul of Numbers :" << mul;
    cout << "\nDiv of Numbers :" << div;
    cout << "\nRem of Numbers :" << rem;

}